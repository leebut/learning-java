public class Birthday {
    // instance vars
    private String cake ;
    private String balloons;
    private int age;

    // getters
    public String getCake(){
        return cake;
    }
    public String getBalloons() {
        return balloons;
    }
    public int getAge(){
        return age;
    }

    // setters
    public void setCake(String newCake) {
        this.cake = newCake;
    }
    public void setBalloons(String newBalloons) {
        this.balloons = newBalloons;
    }
    public void setAge(int newAge) {
        this.age = newAge;
    }
}


